'''Sao alguns exemplos de itens desta bilbiotecas: 
    1. ceil: arredondamento para cima; 
    2. floor: arredondamento para baixo;
    3. trunc: tuncar: elimina todos os numeros após a virgula; 
    4. pow: exponenciacao 
    5. sqrt: raiz quadrada; 
    6. factorial: calcula o fatorial;

Dessa forma, caso queira ausar qualquer um desses itens separadamente é possível usando o comando 
from math import (item) ceil; 

É possível tambem uimportar mais de uma funcao quando necessário: 
from math import sqrt, ceil;
'''